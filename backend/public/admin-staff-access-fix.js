(() => {
  const API = '/api';
  const getToken = () => localStorage.getItem('hotelToken') || localStorage.getItem('adminToken') || localStorage.getItem('token') || localStorage.getItem('abobby_admin_token') || '';
  const authHeaders = () => ({ 'Content-Type': 'application/json', Authorization: `Bearer ${getToken()}` });
  const money = (value) => '₦' + Number(value || 0).toLocaleString();

  async function request(path, options = {}) {
    const res = await fetch(API + path, { ...options, headers: { ...authHeaders(), ...(options.headers || {}) } });
    const data = await res.json().catch(() => ({}));
    if (!res.ok) throw new Error(data.message || 'Request failed');
    return data;
  }

  function alertBox(message, type = 'success') {
    const box = document.getElementById('adminAlert');
    if (!box) return alert(message);
    box.className = `admin-alert ${type}`;
    box.textContent = message;
    box.style.display = 'block';
    setTimeout(() => { box.style.display = 'none'; }, 5000);
  }

  function keepAdminClean() {
    const shortcut = document.getElementById('adminErpShortcut');
    if (shortcut) shortcut.remove();

    ['expenseErpCard', 'inventoryErpCard', 'erpWorkspaceOrganizer'].forEach((id) => {
      const el = document.getElementById(id);
      if (el) el.style.display = 'none';
    });

    const staffCard = document.getElementById('staffErpCard');
    if (staffCard) {
      staffCard.style.display = 'block';
      const title = staffCard.querySelector('h2');
      const desc = staffCard.querySelector('.section-title .muted');
      if (title) title.textContent = 'Staff Login Access';
      if (desc) desc.textContent = 'Create staff login details, assign role, and give each staff a unique PIN generated by Admin.';
    }
  }

  async function regeneratePin(id) {
    if (!confirm('Generate a new PIN for this staff? The old PIN will stop working.')) return;
    try {
      const data = await request(`/staff/${id}`, { method: 'PATCH', body: JSON.stringify({ regenerate_pin: true }) });
      alertBox(`New staff PIN generated: ${data.staff?.staff_pin || 'Done'}`);
      setTimeout(renderStaffPins, 500);
    } catch (err) {
      alertBox(err.message, 'error');
    }
  }

  function addPinStyles() {
    if (document.getElementById('staffPinFixStyles')) return;
    const style = document.createElement('style');
    style.id = 'staffPinFixStyles';
    style.textContent = `
      .staff-pin-badge{display:inline-flex;align-items:center;justify-content:center;min-width:82px;padding:8px 10px;border-radius:12px;background:#111827;color:#facc15;font-weight:1000;letter-spacing:.08em}
      .staff-login-details{font-size:12px;color:#64748b;margin-top:4px;line-height:1.45}
      .staff-access-note{padding:14px 16px;border-radius:16px;background:#eff6ff;border:1px solid #bfdbfe;color:#1e3a8a;font-weight:800;margin:12px 0 16px}
    `;
    document.head.appendChild(style);
  }

  async function renderStaffPins() {
    keepAdminClean();
    addPinStyles();

    const card = document.getElementById('staffErpCard');
    const table = document.getElementById('staffTable');
    if (!card || !table || !getToken()) return;

    if (!document.getElementById('staffAccessNote')) {
      const note = document.createElement('div');
      note.id = 'staffAccessNote';
      note.className = 'staff-access-note';
      note.textContent = 'Staff must login with Email + Password + Unique PIN. PIN is generated and controlled by Admin only.';
      const form = document.getElementById('staffForm');
      if (form) card.insertBefore(note, form);
    }

    try {
      const data = await request('/staff');
      const staff = data.staff || [];
      if (!staff.length) {
        table.innerHTML = '<tr><td colspan="7">No staff accounts yet. Create the first staff login above.</td></tr>';
        return;
      }

      table.innerHTML = `
        <tr>
          <th>Name</th>
          <th>Login Email</th>
          <th>Role</th>
          <th>Unique PIN</th>
          <th>Salary</th>
          <th>Status</th>
          <th>Actions</th>
        </tr>
        ${staff.map((s) => `
          <tr>
            <td><strong>${s.name || '-'}</strong><div class="staff-login-details">${s.department || s.job_title || ''}</div></td>
            <td>${s.email || '-'}</td>
            <td><span class="pill">${s.role || '-'}</span></td>
            <td><span class="staff-pin-badge">${s.staff_pin || 'Not set'}</span></td>
            <td>${s.salary === null || s.salary === undefined ? 'Hidden' : money(s.salary)}</td>
            <td><span class="pill ${s.status === 'active' ? 'paid' : 'cancelled'}">${s.status || 'active'}</span></td>
            <td><button type="button" class="warning staff-new-pin-btn" data-id="${s.id}">Generate New PIN</button></td>
          </tr>
        `).join('')}
      `;

      table.querySelectorAll('.staff-new-pin-btn').forEach((btn) => {
        btn.addEventListener('click', () => regeneratePin(btn.dataset.id));
      });
    } catch (err) {
      table.innerHTML = `<tr><td colspan="7">${err.message}</td></tr>`;
    }
  }

  function boot() {
    keepAdminClean();
    setTimeout(renderStaffPins, 1200);
    setInterval(() => {
      keepAdminClean();
      const table = document.getElementById('staffTable');
      if (table && !table.dataset.pinFixed) {
        table.dataset.pinFixed = 'true';
        renderStaffPins();
      }
    }, 1800);

    const staffForm = document.getElementById('staffForm');
    if (staffForm) staffForm.addEventListener('submit', () => setTimeout(renderStaffPins, 1000));
  }

  window.renderStaffPins = renderStaffPins;
  if (document.readyState === 'loading') document.addEventListener('DOMContentLoaded', boot);
  else boot();
})();
